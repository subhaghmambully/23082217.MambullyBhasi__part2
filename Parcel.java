public class Parcel {
    private String parcelId;
    private int weight;
    private int length;
    private int width;
    private int height;
    private double distance;

    // Constructor
    public Parcel(String parcelId, int weight, int length, int width, int height, double distance) {
        this.parcelId = parcelId;
        this.weight = weight;
        this.length = length;
        this.width = width;
        this.height = height;
        this.distance = distance;
    }

  
}
